    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        JPanel panel = new JPanel(new java.awt.GridLayout(0, 1));
        JTextField titleField = new JTextField();
        JTextField authorField = new JTextField();
        JTextField yearField = new JTextField();
        JTextField priceField = new JTextField();
        JTextField categoryField = new JTextField();
        JTextField amountField = new JTextField();
        JTextField availableField = new JTextField();

        panel.add(new JLabel("Название:")); panel.add(titleField);
        panel.add(new JLabel("Автор:")); panel.add(authorField);
        panel.add(new JLabel("Год:")); panel.add(yearField);
        panel.add(new JLabel("Цена:")); panel.add(priceField);
        panel.add(new JLabel("Категория:")); panel.add(categoryField);
        panel.add(new JLabel("Количество всего:")); panel.add(amountField);
        panel.add(new JLabel("Доступно:")); panel.add(availableField);

        int result = JOptionPane.showConfirmDialog(null, panel, "Добавить книгу",
            JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE);

        if (result == JOptionPane.OK_OPTION) {
            try {
                library.addBook(
                    titleField.getText(),
                    authorField.getText(),
                    yearField.getText(),
                    Double.parseDouble(priceField.getText()),
                    categoryField.getText(),
                    Integer.parseInt(amountField.getText()),
                    Integer.parseInt(availableField.getText())
                );
                refreshTable(library.getAllBooks());
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Ошибка ввода: " + ex.getMessage());
            }
        }
    }//GEN-LAST:event_addButtonActionPerformed

