    private void refreshTable(List<Book> books) {
        tableModel.setRowCount(0); 
        for (Book b : books) {
            tableModel.addRow(new Object[]{
                b.getId(),
                b.getTitle(),
                b.getAuthor(),
                b.getYear(),
                b.getPrice(),
                b.getCategory(),
                b.getAmount(),
                b.getAvailable()
            });
        }
    }
    
    private void initLibraryData() {
        String[] columnNames = {"ID", "Название", "Автор", "Год", "Цена", "Категория", "Всего", "Доступно"};
        tableModel = new DefaultTableModel(columnNames, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        LibraryTable.setModel(tableModel);

        try {
            library = new Library();
            library.loadAndValidate();
            refreshTable(library.getAllBooks());
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ошибка при загрузке данных: " + e.getMessage());
            e.printStackTrace();
        }
    }
    
