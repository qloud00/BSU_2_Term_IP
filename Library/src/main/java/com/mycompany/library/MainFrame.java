    private void renameButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_renameButtonActionPerformed
        int selectedRow = LibraryTable.getSelectedRow();
        if (selectedRow >= 0) {
            int id = (int) tableModel.getValueAt(selectedRow, 0); // ID в 0 колонке
            String currentPrice = tableModel.getValueAt(selectedRow, 4).toString();
            
            String newPriceStr = JOptionPane.showInputDialog(this, 
                    "Текущая цена: " + currentPrice + "\nВведите новую цену:", currentPrice);
            
            if (newPriceStr != null) {
                try {
                    double newPrice = Double.parseDouble(newPriceStr);
                    library.updateBookPrice(id, newPrice);
                    refreshTable(library.getAllBooks());
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, "Некорректная цена!");
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Выберите книгу в таблице!");
        }
    }//GEN-LAST:event_renameButtonActionPerformed

    private void issueButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_issueButtonActionPerformed
        int selectedRow = LibraryTable.getSelectedRow();
        if (selectedRow >= 0) {
            int id = (int) tableModel.getValueAt(selectedRow, 0);
            try {
                boolean success = library.issueBook(id);
                if (success) {
                    JOptionPane.showMessageDialog(this, "Книга выдана!");
                    refreshTable(library.getAllBooks());
                } else {
                    JOptionPane.showMessageDialog(this, "Нет доступных экземпляров.");
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Ошибка: " + ex.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Выберите книгу для выдачи!");
        }
    }//GEN-LAST:event_issueButtonActionPerformed

    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        String query = JOptionPane.showInputDialog(this, "Введите текст для поиска:");
        
        if (query != null) {
            if (query.trim().isEmpty()) {
                refreshTable(library.getAllBooks()); // Сброс фильтра
            } else {
                int criteriaType = searchBox.getSelectedIndex(); // 0-Автор, 1-Год, 2-Категория
                try {
                    List<Book> results = library.searchBooks(query, criteriaType);
                    refreshTable(results);
                    if (results.isEmpty()) {
                        JOptionPane.showMessageDialog(this, "Ничего не найдено.");
                    }
                } catch (NumberFormatException ex) {
                    JOptionPane.showMessageDialog(this, "Для поиска по году введите число!");
                }
            }
        }
    }//GEN-LAST:event_searchButtonActionPerformed

